package com.dreamer.tool.database;

public class LoveChannelData {
	
	/** 数据类型 long */
	private long frequency;
	/** 数据类型 int */
	private int program_number;
	/** 数据类型 boolean */
	private int love_status;
	
	public long getFrequency() {
		return frequency;
	}
	public void setFrequency(long frequency) {
		this.frequency = frequency;
	}
	public int getProgram_number() {
		return program_number;
	}
	public void setProgram_number(int program_number) {
		this.program_number = program_number;
	}
	public int getLove_status() {
		return love_status;
	}
	public void setLove_status(int love_status) {
		this.love_status = love_status;
	}
	
	
}
